package laboratorio.pkg02;

public class Operaciones {
    public double respuesta;
    public void suma(double dato1,double dato2){
        respuesta=dato1+dato2;
        
    }
    public void resta(double dato1,double dato2){
        respuesta=dato1-dato2;
    }
    public void multiplicacion(double dato1,double dato2){
        respuesta=dato1*dato2;
    }
    public void division(double dato1,double dato2){
        respuesta=dato1/dato2;
    }
    
}
